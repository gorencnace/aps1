class Robot {
    int x;
    int y;
    int smer;
    
    public Robot(int x, int y, int smer) {
	this.x = x;
	this.y = y;
	this.smer = smer;
    }
    
    public int getX() {
	return x;
    }

    public void setX(int x) {
	this.x = x;
    }

    public int getY() {
	return y;
    }

    public void setY(int y) {
	this.y = y;
    }

    public int getSmer() {
	return smer;
    }

    public void setSmer(int smer) {
	this.smer = smer;
    }
}
